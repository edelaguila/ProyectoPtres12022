    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Bitacora");
        modelo.addColumn("Fecha");
        modelo.addColumn("Accion");
        modelo.addColumn("Id_Usuario");
        modelo.addColumn("Id_Aplicacion");
        modelo.addColumn("IP");
        modelo.addColumn("Nompre PC");
        daoBitacora bitacoraDAO = new daoBitacora();
        List<clsBitacora> bitacoras = bitacoraDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[7];
        for (int i = 0; i < bitacoras.size(); i++) {
            dato[0] = Integer.toString(bitacoras.get(i).fGetId_bitacora());
            dato[1] = bitacoras.get(i).fGetfecha_Bitacora();
            dato[2] = bitacoras.get(i).fGetaccion_Bitacora();
            dato[3] = Integer.toString(bitacoras.get(i).fGetId_Usuario());
            dato[4] = Integer.toString(bitacoras.get(i).fGetId_Aplicacion());
            dato[5] = bitacoras.get(i).fGetip_Bitacora();
            dato[6] = bitacoras.get(i).fGetnombrepc_Bitacora();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscarAplicacion() {
        clsBitacora bitacoraAConsultar = new clsBitacora();
        daoBitacora bitacoraDAO = new daoBitacora();
        bitacoraAConsultar.fSetId_Bitacora(Integer.parseInt(txtbuscado.getText()));
        bitacoraAConsultar = bitacoraDAO.query(bitacoraAConsultar);
        txtFecha.setText(bitacoraAConsultar.fGetfecha_Bitacora());
        txtAccion.setText(bitacoraAConsultar.fGetaccion_Bitacora());
        txtIdusuario.setText(String.valueOf(bitacoraAConsultar.fGetId_Usuario()));
        txtIdaplicacion.setText(String.valueOf(bitacoraAConsultar.fGetId_Aplicacion()));
        txtIp.setText(String.valueOf(bitacoraAConsultar.fGetip_Bitacora()));
        txtNombrepc.setText(String.valueOf(bitacoraAConsultar.fGetnombrepc_Bitacora()));
    }

